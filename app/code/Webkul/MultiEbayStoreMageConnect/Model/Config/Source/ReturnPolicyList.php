namespace Webkul\MultiEbayStoreMageConnect\Model\Config\Source;

class ReturnPolicyList implements \Magento\Framework\Option\ArrayInterface
{
    /**
     * Return options array.
     *
     * @param int $store
     *
     * @return array
     */
    public function toOptionArray($store = null)
    {
        $returnPolicyList = [['value' => 'ReturnsAccepted','label' => 'Returns Accepted'],
                             ['value' => 'ReturnsNotAccepted','label' => 'Returns Not Accepted'], ];

        return $returnPolicyList;
    }

    /**
     * Get options in "key-value" format.
     *
     * @return array
     */
    public function toArray()
    {
        $optionList = $this->toOptionArray();
        $optionArray = [];
        foreach ($optionList as $option) {
            $optionArray[$option['value']] = $option['label'];
        }

        return $optionArray;
    }
}
